/**
 * Cette énumération représente les différentes catégories de voiliers multicoques.
 * Elle contient des valeurs qui correspondent aux classes spécifiques de voiliers multicoques.
 */
public enum EnumMulti {

    /**
     * Multi 50 : Voilier multicoque de 50 pieds utilisé dans des courses comme la Route du Rhum.
     */
    Multi50,

    /**
     * Ultim : Voilier multicoque utilisé pour les courses en haute mer, comme le Trophée Jules Verne.
     */
    Ultim,

    /**
     * RhumMulti : Voilier multicoque participant à la Route du Rhum.
     */
    RhumMulti
}
