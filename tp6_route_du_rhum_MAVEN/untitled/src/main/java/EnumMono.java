/**
 * Cette énumération représente les différentes catégories de voiliers monocoques.
 * Elle contient des valeurs qui correspondent aux classes spécifiques de voiliers monocoques.
 */
public enum EnumMono {

    /**
     * Classe 40 : Voilier monocoque de 40 pieds utilisé dans la course au large.
     */
    Class40,

    /**
     * Imoca : Voilier monocoque utilisé dans les courses telles que le Vendée Globe.
     */
    Imoca,

    /**
     * RhumMono : Voilier monocoque participant à la Route du Rhum.
     */
    RhumMono
}
